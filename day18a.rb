STEPS = 100

initial_lights = Hash.new(:off)

DATA.read.each_line.with_index do |line, i|
  row = i + 1

  line.strip.chars.each.with_index do |char, j|
    col = j + 1

    initial_lights[[row, col]] = (char == "#" ? :on : :off)
  end
end

def neighbours(lights, (line, col))
  lights.values_at *[
    [ line.pred, col.pred ],
    [ line.pred, col ],
    [ line.pred, col.succ ],
    [ line, col.pred ],
    [ line, col.succ ],
    [ line.succ, col.pred ],
    [ line.succ, col ],
    [ line.succ, col.succ ],
  ]
end

def update_state(old_lights)
  old_lights.map { |coord, old_value|

    count_of_lit_neighbours = neighbours(old_lights, coord).count { |state| state == :on }

    new_value =
      if old_value == :on && [ 2, 3 ].include?(count_of_lit_neighbours)
        :on
      elsif old_value == :off && [ 3 ].include?(count_of_lit_neighbours)
        :on
      else
        :off
      end

    [ coord, new_value ]
  }.to_h
end

lights = initial_lights
STEPS.times { lights = update_state(lights) }

p lights.count { |k, v| v == :on }

